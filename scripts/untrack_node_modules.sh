#!/usr/bin/env bash
set -euo pipefail
cd "$(dirname "$0")/.." || exit 1
BRANCH=$(git rev-parse --abbrev-ref HEAD 2>/dev/null || echo unknown)
echo "On branch: $BRANCH"
# ensure .gitignore has node_modules entries
touch .gitignore
if ! grep -qxF 'node_modules/' .gitignore; then
  echo 'node_modules/' >> .gitignore
fi
if ! grep -qxF '**/node_modules/' .gitignore; then
  echo '**/node_modules/' >> .gitignore
fi

echo "Finding node_modules directories..."
# Untrack each node_modules dir
find . -type d -name node_modules -prune -print0 | while IFS= read -r -d '' dir; do
  git rm -r --cached --ignore-unmatch "$dir" || true
done

# Stage .gitignore
git add .gitignore

# Commit if there are staged changes
if git diff --cached --quiet; then
  echo "No changes to commit"
else
  git commit -m "chore: add .gitignore and untrack node_modules"
fi

echo "Remaining changed files:"
git ls-files -m -o --exclude-standard | wc -l
